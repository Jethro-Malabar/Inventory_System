public class Output {

    private String nameOfHardware;
    private String code;

    Output(String nameOfHardware, String code) {

        this.code = code;
        this.nameOfHardware = nameOfHardware;
    }

    public String getNameOfHardware() {
        return nameOfHardware;
    }
    public String getCode() {
        return code;
    }

}
